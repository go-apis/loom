		fmt.Fprintf(&b, "// %s implements %s.%sHandlers. Yours to edit — loom generate never\n// rewrites this file.\n", a.Name, g.genPkg, a.Name)
		fmt.Fprintf(&b, "type %s struct{}\n\n", a.Name)
